    public FrmRegister(Access a) {
        initComponents();
        
        if (a == null) {
            btnSearchRegister.setVisible(false);
            btnDeleteRegister.setVisible(false);
            btnUpdateRegister.setVisible(false);
            chkPermission.setVisible(false);
        }
        else {
            if (a.getPermission() == 0) {
                btnNewRegister.setVisible(false);
                btnSaveRegister.setVisible(false);
                btnSearchRegister.setVisible(false);
                btnDeleteRegister.setVisible(false);
                chkPermission.setVisible(false);
            }
            
            loged = a;
            access = a;
            fillFieldsRegister();
        }
    }
    
    public void limpaCamposRegister() {
        access = null;
        txtUsername.setText("");
        txtPassword.setText("");
        txtConfirmPassword.setText("");
        chkPermission.setSelected(false);
    }
    
    public void habilitaDesabilitaCamposRegister(boolean habilita) {
        txtUsername.setEnabled(habilita);
        txtPassword.setEnabled(habilita);
        txtConfirmPassword.setEnabled(habilita);
        chkPermission.setEnabled(habilita);
        btnSearchRegister.setEnabled(habilita);
        btnSearchRegister.setSelected(false);
    }
    
    public void fillFieldsRegister() {
        txtUsername.setText(access.getUsername());
        chkPermission.setSelected(access.getPermission() == 1);
    }

