    private void btnNewRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnNewRegisterMouseClicked
        if (btnNewRegister.isEnabled()) {
            limpaCamposRegister();
            habilitaDesabilitaCamposRegister(true);

            btnNewRegister.setEnabled(false);
            btnUpdateRegister.setEnabled(false);
            btnDeleteRegister.setEnabled(false);
            btnCancelRegister.setEnabled(true);
            btnSaveRegister.setEnabled(true);
            btnExitRegister.setEnabled(false);
        }
    }//GEN-LAST:event_btnNewRegisterMouseClicked

    private void btnUpdateRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateRegisterMouseClicked
        if (btnUpdateRegister.isEnabled()) {
            String passkey = new String(txtPassword.getPassword());
            int permission = 0;
            
            if (!passkey.equals(new String(txtConfirmPassword.getPassword()))) {
                JOptionPane.showMessageDialog(jpRegister, "invalid passkey confirmation");
            }
            else {
                if (chkPermission.isVisible()) permission = chkPermission.isSelected()?1:0;
                access = new Access(access.getIdAccess(), access.getUsername(), 
                        passkey, permission);

                if (acC.update(access) == 1) {
                    JOptionPane.showMessageDialog(jpRegister, "success!");
                    limpaCamposRegister();
                    habilitaDesabilitaCamposRegister(rootPaneCheckingEnabled);

                    btnNewRegister.setEnabled(true);
                    btnUpdateRegister.setEnabled(false);
                    btnDeleteRegister.setEnabled(false);
                    btnCancelRegister.setEnabled(false);
                    btnSaveRegister.setEnabled(false);
                    btnExitRegister.setEnabled(true);

                    if (loged.getPermission() == 0) {
                        this.setVisible(false);
                    }
                }
                else {
                    JOptionPane.showMessageDialog(jpRegister, "error");
                }
            }
        }
    }//GEN-LAST:event_btnUpdateRegisterMouseClicked

    private void btnDeleteRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteRegisterMouseClicked
        if (btnDeleteRegister.isEnabled()) {
            if (acC.remove(access) == 1) {
                JOptionPane.showMessageDialog(jpRegister, "success!");
                limpaCamposRegister();
                habilitaDesabilitaCamposRegister(false);

                btnNewRegister.setEnabled(true);
                btnUpdateRegister.setEnabled(false);
                btnDeleteRegister.setEnabled(false);
                btnCancelRegister.setEnabled(false);
                btnSaveRegister.setEnabled(false);
                btnExitRegister.setEnabled(true);
            }
            else {
                JOptionPane.showMessageDialog(jpRegister, "error");
            }
        }
    }//GEN-LAST:event_btnDeleteRegisterMouseClicked

    private void btnSaveRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveRegisterMouseClicked
        if (btnSaveRegister.isEnabled()) {
            String passkey = new String(txtPassword.getPassword());
            int permission = 0;
            System.out.println(passkey);
            System.out.println(new String(txtConfirmPassword.getPassword()));
            if (acC.getByLogin(txtUsername.getText()) != null) {
                JOptionPane.showMessageDialog(jpRegister, "invalid login! choose another one");
            }
            else {
                if (!passkey.equals(new String(txtConfirmPassword.getPassword()))) {
                    JOptionPane.showMessageDialog(jpRegister, "invalid passkey confirmation");
                }
                else {
                    if (chkPermission.isVisible()) permission = chkPermission.isSelected()?1:0;
                    access = new Access(txtUsername.getText(), passkey, permission);

                    if (acC.insert(access) == 1) {
                        JOptionPane.showMessageDialog(jpRegister, "success!");

                        limpaCamposRegister();
                        habilitaDesabilitaCamposRegister(false);

                        //gerenciando os botoes
                        btnNewRegister.setEnabled(true);
                        btnUpdateRegister.setEnabled(false);
                        btnDeleteRegister.setEnabled(false);
                        btnCancelRegister.setEnabled(false);
                        btnSaveRegister.setEnabled(false);
                        btnExitRegister.setEnabled(true);

                        if (loged == null) {
                            this.setVisible(false);
                        }
                        else {
                            if (loged.getPermission() == 0) {
                                this.setVisible(false);
                            }
                        }
                    }
                    else {
                        JOptionPane.showMessageDialog(jpRegister, "error");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnSaveRegisterMouseClicked

    private void btnCancelRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCancelRegisterMouseClicked
        if (btnCancelRegister.isEnabled()) {
            limpaCamposRegister();
            habilitaDesabilitaCamposRegister(true);
            //Habilitando a barra de ferramentas
            btnNewRegister.setEnabled(false);
            btnUpdateRegister.setEnabled(false);
            btnDeleteRegister.setEnabled(false);
            btnCancelRegister.setEnabled(true);
            btnSaveRegister.setEnabled(true);
            btnExitRegister.setEnabled(true);
        }
    }//GEN-LAST:event_btnCancelRegisterMouseClicked

    private void btnSearchRegisterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchRegisterMouseClicked
        if (btnSearchRegister.isEnabled()) {
            access = acC.getByLogin(txtUsername.getText());
            
            if (access == null) {
                JOptionPane.showMessageDialog(jpRegister, "invalid login");
                btnSearchRegister.setSelected(false);
            }
            else {
                fillFieldsRegister();
                txtUsername.setEnabled(false);
                btnSearchRegister.setEnabled(false);

                btnNewRegister.setEnabled(false);
                btnUpdateRegister.setEnabled(true);
                btnDeleteRegister.setEnabled(true);
                btnCancelRegister.setEnabled(true);
                btnSaveRegister.setEnabled(false);
                btnExitRegister.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnSearchRegisterMouseClicked

